------------------------------------------------
--CREATE CUSTOMERS TABLE
--------------------------------------------------------------------------
CREATE TABLE CUSTOMERS(
    CUSTOMER_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CUSTOMER_NAME VARCHAR(50),
    EMAIL         VARCHAR(50),
    CITY          VARCHAR(50)
);


---------------------------------------------------------
-- STEP 1: CREATE PRODUCTS TABLE
---------------------------------------------------------
CREATE TABLE PRODUCTS(
    PRODUCT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRODUCT_NAME VARCHAR(50),
    CATEGORY     VARCHAR(50),
    PRICE        NUMBER
);


---------------------------------------------------------
-- STEP 1: CREATE SALES TABLE
---------------------------------------------------------

CREATE TABLE SALES(
    SALES_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CUSTOMER_ID NUMBER,
    PRODUCT_ID NUMBER,
    SALES_DATE DATE DEFAULT SYSDATE,
    QUANTITY NUMBER,
    SALES_AMOUNT NUMBER,
    CONSTRAINT FK_SALES_CUSTOMERS FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS(CUSTOMER_ID),
    CONSTRAINT FK_SALES_PRODUCTS FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(PRODUCT_ID)

);
